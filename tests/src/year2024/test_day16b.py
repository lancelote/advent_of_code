"""2024 - Day 16 Part 2: Reindeer Maze"""

from textwrap import dedent

from src.year2024.day16b import solve


def test_solve_first():
    task = dedent(
        """
        ###############
        #.......#....E#
        #.#.###.#.###.#
        #.....#.#...#.#
        #.###.#####.#.#
        #.#.#.......#.#
        #.#.#####.###.#
        #...........#.#
        ###.#.#####.#.#
        #...#.....#.#.#
        #.#.#.###.#.#.#
        #.....#...#.#.#
        #.###.#.#.#.#.#
        #S..#.....#...#
        ###############
        """
    ).strip()
    assert solve(task) == 45


